public class CWH_16_Conditionals {
    public static void main(String[] args) {
        //greater than:
        int age1 = 6;
        if (age1>18){
            System.out.println("Yes boy you can drive!");
        }
        else{
            System.out.println("No boy you cannot drive yet!");
        }
        //not equals to:
        int age2 = 18;
        if (age2!=18){
            System.out.println("Yes boy you can drive");
        }
        else{
            System.out.println("No boy you cannot drive");
        }
        //equals to equals to:
        int age3 = 18;
        if (age3 == 18){
            System.out.println("Yes boy you can drive");
        }
        else{
            System.out.println("No boy you cannot drive");
        }


    }
}